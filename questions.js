// Fisher-Yates shuffle algorithm
function shuffleArray(array) {
    const newArray = [...array];
    for (let i = newArray.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [newArray[i],
            newArray[j]] = [newArray[j],
            newArray[i]];
    }
    return newArray;
}

// Generate randomized test with scrambled questions and options
function generateRandomizedTest() {
    // 1. Shuffle the question order
    const shuffledQuestions = shuffleArray(questionBank);

    // 2. Process each question to shuffle options
    const randomizedQuestions = shuffledQuestions.map(q => {
        // Create array of option indices
        const optionIndices = q.options.map((_, i) => i);

        // Shuffle the option indices
        const shuffledIndices = shuffleArray(optionIndices);

        // Create new options array in shuffled order
        const scrambledOptions = shuffledIndices.map(i => q.options[i]);

        // Find new position of correct answer
        const newAnswerIndex = shuffledIndices.indexOf(q.answer);

        return {
            section: q.section,
            question: q.question,
            options: scrambledOptions,
            answer: newAnswerIndex,
            explanation: q.explanation
        };
    });

    return randomizedQuestions;
}

